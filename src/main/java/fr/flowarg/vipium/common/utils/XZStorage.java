package fr.flowarg.vipium.common.utils;

public class XZStorage
{
    private final double x;
    private final double z;

    public XZStorage(double x, double z)
    {
        this.x = x;
        this.z = z;
    }

    public double getX()
    {
        return this.x;
    }

    public double getZ()
    {
        return this.z;
    }
}
